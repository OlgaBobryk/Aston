package Lesson4;

public class Person {
    String nameOfPerson;
    int ageOfPerson;
    String positionOfPerson;
    String phoneOfPerson;
    String emailOfPerson;
    int salaryOfPerson;

    public Person(String nameOfPerson, int ageOfPerson, String positionOfPerson, String phoneOfPerson,
                  String emailOfPerson, int salaryOfPerson) {
        this.nameOfPerson = nameOfPerson;
        this.ageOfPerson = ageOfPerson;
        this.positionOfPerson = positionOfPerson;
        this.phoneOfPerson = phoneOfPerson;
        this.emailOfPerson = emailOfPerson;
        this.salaryOfPerson = salaryOfPerson;

    }
}